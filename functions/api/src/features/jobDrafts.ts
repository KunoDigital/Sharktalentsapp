const log = logger('JOB_DRAFTS');
const TABLE = 'JobProfileDrafts';

export type DraftStatus =
  | 'draft_generated'
  | 'pending_client_review'
  | 'client_approved'
  | 'client_changes_requested'
  | 'converted_to_job'
  | 'discarded';

export type JobProfileDraftRow = {
  ROWID: string;
  tenant_id: string;
  transcript: string | null;
  transcript_source: string;
  meeting_url: string | null;
  draft_payload: string;
  status: DraftStatus;
  version: number;
  highlights: string | null;
  created_by: string;
  client_email: string | null;
  client_approved_at: string | null;
  job_id: string | null;
  created_at: string;
  updated_at: string;
};

const TABLE_NOT_READY = new AppError(
  503,
  'table_not_ready',
  `La tabla ${TABLE} todavía no fue creada. Crear en Catalyst Console (ver MIGRATIONS_BLOCK2.md §4) y reintentar.`,
);

async function isTableReady(req: IncomingMessage): Promise<boolean> {
  try {
    await zcql(req).executeZCQLQuery(`SELECT ROWID FROM ${TABLE} LIMIT 1`);
    return true;
  } catch (err) {
    log.warn('table not ready', { error: (err as Error).message });
    return false;
  }
}

function extractIdFromPath(url: string, suffix = ''): string | null {
  const re = suffix
    ? new RegExp(`^/api/drafts/jobs/([^/]+)${suffix}/?$`)
    : /^\/api\/drafts\/jobs\/([^/]+)\/?$/;
  return url.match(re)?.[1] ?? null;
}

function validateStatus(s: unknown): DraftStatus {
  const allowed: DraftStatus[] = [
    'draft_generated', 'pending_client_review', 'client_approved',
    'client_changes_requested', 'converted_to_job', 'discarded',
  ];
  if (typeof s !== 'string' || !allowed.includes(s as DraftStatus)) {
    throw new ValidationError(`status must be one of ${allowed.join(', ')}`);
  }
  return s as DraftStatus;
}

