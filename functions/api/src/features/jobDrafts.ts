/**
 * Persistencia de drafts de Job Profile (post-reunión cliente).
 *
 * La tabla `JobProfileDrafts` es OPCIONAL (deferred Block 2). Si no existe, los
 * endpoints devuelven 503 con un mensaje claro.
 *
 * Endpoints:
 *   POST  /api/drafts/jobs/save         → persiste el draft generado por IA
 *   GET   /api/drafts/jobs              → lista drafts del tenant
 *   GET   /api/drafts/jobs/:id          → obtiene un draft
 *   PATCH /api/drafts/jobs/:id          → actualiza status/draft_payload (regeneración manual)
 *   POST  /api/drafts/jobs/:id/convert  → crea Job real desde el draft + linkea draft.job_id
 *
 * Esto NO incluye el flow del portal cliente (approve / request-changes desde el lado del cliente).
 * Eso requiere endpoints públicos con token kind='portal' que validen el draft pertenece al portal,
 * y se hace en otra tanda.
 */
