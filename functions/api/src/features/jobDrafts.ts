/**
 * Cliente aprueba un draft via portal token.
 *
 *   POST /portal/<token>/drafts/<id>/approve
 *   Body opcional: { client_comment?: string }
 *
 * Marca status='client_approved' + client_approved_at=now. Audit log + outbox event
 * para que Cris reciba notificación.
 */
export async function approveDraftPublic(ctx: RequestContext): Promise<void> {
  const match = ctx.req.url?.match(/^\/portal\/([^/]+)\/drafts\/([^/]+)\/approve/);
  const [, token, draftId] = match ?? [];
  if (!token || !draftId) throw new ValidationError('path inválido');

  // Verificar portal token
  let claims;
  try {
    const { verifyPortalToken } = await import('../lib/clientPortalTokens.js');
    claims = verifyPortalToken(token);
  } catch {
    throw new AppError(401, 'unauthorized', 'Portal token inválido o expirado');
  }

  if (!(await isTableReady(ctx.req))) throw TABLE_NOT_READY;

  // Cargar draft (público, sin tenantId — el portal token ya verifica acceso)
  const q = `SELECT * FROM ${TABLE} WHERE ROWID = '${escapeSql(draftId)}' LIMIT 1`;
  const rows = unwrapRows<JobProfileDraftRow>(
    (await zcql(ctx.req).executeZCQLQuery(q)) as unknown[],
    TABLE,
  );
  const draft = rows[0];
  if (!draft) throw new NotFoundError(`Draft ${draftId} not found`);

  // Verificar que el portal token es del mismo tenant que el draft.
  // Portal token guarda tenant_id en claims.ref (ver lib/clientPortalTokens.ts).
  if (claims.ref !== draft.tenant_id) {
    throw new AppError(403, 'forbidden', 'Portal token no pertenece al tenant del draft');
  }

  const body = await readJsonBody<{ client_comment?: string }>(ctx.req).catch(() => ({} as { client_comment?: string }));
  const comment = typeof body.client_comment === 'string' ? body.client_comment.slice(0, 500) : null;

  await datastore(ctx.req).table(TABLE).updateRow({
    ROWID: draftId,
    status: 'client_approved',
    updated_at: now(),
  });

  // Convertir el draft a Job real + sincronizar con Recruit. Idempotente: si ya está
  // convertido (job_id seteado) usa el existente. Si falla, dejamos el draft como
  // 'client_approved' (NO 'converted_to_job') y log warning — Cris puede re-disparar
  // el convert manualmente desde admin.
  let jobId: string | null = null;
  try {
    const result = await convertDraftInternal(ctx, draft, draft.tenant_id, { createdBy: 'client_portal' });
    jobId = result.jobId;
  } catch (err) {
    log.error('approve→convert failed', {
      traceId: ctx.traceId, draftId, error: (err as Error).message,
    });
    // No throw — el approve igual fue exitoso. Cris convierte manual después.
  }

  // Outbox: notificar a Cris
  try {
    const { publishOutboxEvent } = await import('./outbox.js');
    await publishOutboxEvent(ctx.req, 'draft.client_approved', {
      tenant_id: draft.tenant_id,
      draft_id: draftId,
      client_email: draft.client_email,
      client_comment: comment,
      job_id: jobId,
    });
  } catch (err) {
    log.warn('draft approved outbox publish failed', { error: (err as Error).message });
  }

  // Tracking server-side
  void (async () => {
    const { recordPortalSnapshot } = await import('./jobTracking.js');
    await recordPortalSnapshot(ctx, {
      tenantId: draft.tenant_id,
      eventType: 'portal.draft_approved',
      portalToken: token,
      eventData: { draft_id: draftId, has_comment: !!comment, job_id: jobId },
    });
  })();

  log.info('draft approved by client + converted to job', { draftId, tenantId: draft.tenant_id, jobId });
  sendJson(ctx.res, 200, { ok: true, status: 'client_approved', job_id: jobId });
}

/**
 * Cliente rechaza/pide cambios en un draft.
 *
 *   POST /portal/<token>/drafts/<id>/request-changes
 *   Body: { client_comment: string (qué cambiar) }
 */
export async function requestChangesDraftPublic(ctx: RequestContext): Promise<void> {
  const match = ctx.req.url?.match(/^\/portal\/([^/]+)\/drafts\/([^/]+)\/request-changes/);
  const [, token, draftId] = match ?? [];
  if (!token || !draftId) throw new ValidationError('path inválido');

  let claims;
  try {
    const { verifyPortalToken } = await import('../lib/clientPortalTokens.js');
    claims = verifyPortalToken(token);
  } catch {
    throw new AppError(401, 'unauthorized', 'Portal token inválido o expirado');
  }

  if (!(await isTableReady(ctx.req))) throw TABLE_NOT_READY;

  const q = `SELECT * FROM ${TABLE} WHERE ROWID = '${escapeSql(draftId)}' LIMIT 1`;
  const rows = unwrapRows<JobProfileDraftRow>(
    (await zcql(ctx.req).executeZCQLQuery(q)) as unknown[],
    TABLE,
  );
  const draft = rows[0];
  if (!draft) throw new NotFoundError(`Draft ${draftId} not found`);

  // Portal token guarda tenant_id en claims.ref (ver lib/clientPortalTokens.ts).
  if (claims.ref !== draft.tenant_id) {
    throw new AppError(403, 'forbidden', 'Portal token no pertenece al tenant del draft');
  }

  const body = await readJsonBody<{ client_comment?: string }>(ctx.req);
  const comment = typeof body.client_comment === 'string' ? body.client_comment.trim() : '';
  if (comment.length === 0) {
    throw new ValidationError('client_comment requerido para solicitar cambios');
  }

  // Persistir comentario en el campo `highlights` del draft para que el admin lo vea.
  // Formato: JSON con array de comentarios timestamped — soporta múltiples rondas.
  let existingComments: Array<{ at: string; text: string }> = [];
  try {
    if (draft.highlights) {
      const parsed = JSON.parse(draft.highlights);
      if (parsed && Array.isArray(parsed.client_comments)) {
        existingComments = parsed.client_comments;
      }
    }
  } catch { /* ignore parse error, sobreescribimos */ }
  existingComments.push({ at: now(), text: comment.slice(0, 500) });
  const newHighlights = JSON.stringify({ client_comments: existingComments });

  await datastore(ctx.req).table(TABLE).updateRow({
    ROWID: draftId,
    status: 'pending_client_review',
    highlights: newHighlights,
    updated_at: now(),
  });

  try {
    const { publishOutboxEvent } = await import('./outbox.js');
    await publishOutboxEvent(ctx.req, 'draft.client_requested_changes', {
      tenant_id: draft.tenant_id,
      draft_id: draftId,
      client_email: draft.client_email,
      client_comment: comment.slice(0, 500),
    });
  } catch (err) {
    log.warn('draft request-changes outbox publish failed', { error: (err as Error).message });
  }

  void (async () => {
    const { recordPortalSnapshot } = await import('./jobTracking.js');
    await recordPortalSnapshot(ctx, {
      tenantId: draft.tenant_id,
      eventType: 'portal.draft_rejected',
      portalToken: token,
      eventData: { draft_id: draftId },
    });
  })();

  log.info('draft changes requested by client', { draftId });
  sendJson(ctx.res, 200, { ok: true, status: 'pending_client_review' });
}

