// ===== Helpers: builders de texto enriquecido para Recruit =====

function bulletList(items: unknown): string {
  if (!Array.isArray(items) || items.length === 0) return '';
  const cleaned = items.filter((x): x is string => typeof x === 'string' && x.trim().length > 0);
  if (cleaned.length === 0) return '';
  return '<ul>' + cleaned.map((x) => `<li>${escapeHtml(x)}</li>`).join('') + '</ul>';
}

function escapeHtml(s: string): string {
  return s.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;').replace(/"/g, '&quot;');
}

function buildJobDescriptionHtml(payload: Record<string, unknown>): string {
  const parts: string[] = [];
  const objetivo = typeof payload.objetivo_cargo === 'string' ? payload.objetivo_cargo : '';
  const context = typeof payload.context_summary === 'string' ? payload.context_summary : '';
  if (objetivo) parts.push(`<p><strong>Objetivo del cargo:</strong> ${escapeHtml(objetivo)}</p>`);
  if (context) parts.push(`<p>${escapeHtml(context)}</p>`);
  if (Array.isArray(payload.responsabilidades) && payload.responsabilidades.length > 0) {
    parts.push('<p><strong>Responsabilidades principales:</strong></p>');
    parts.push(bulletList(payload.responsabilidades));
  }
  if (Array.isArray(payload.tareas_especificas) && payload.tareas_especificas.length > 0) {
    parts.push('<p><strong>Tareas específicas:</strong></p>');
    parts.push(bulletList(payload.tareas_especificas));
  }
  return parts.join('');
}

function buildRequisitosHtml(payload: Record<string, unknown>): string {
  const parts: string[] = [];
  const formacion = typeof payload.formacion_requerida === 'string' ? payload.formacion_requerida : '';
  const experiencia = typeof payload.experiencia_requerida === 'string' ? payload.experiencia_requerida : '';
  if (formacion) parts.push(`<p><strong>Formación:</strong> ${escapeHtml(formacion)}</p>`);
  if (experiencia) parts.push(`<p><strong>Experiencia:</strong> ${escapeHtml(experiencia)}</p>`);
  if (Array.isArray(payload.herramientas_conocimientos) && payload.herramientas_conocimientos.length > 0) {
    parts.push('<p><strong>Herramientas y conocimientos técnicos:</strong></p>');
    parts.push(bulletList(payload.herramientas_conocimientos));
  }
  if (Array.isArray(payload.competencias) && payload.competencias.length > 0) {
    const compsArr = payload.competencias as Array<{ name?: string; required_pct?: number; que_evaluamos?: string }>;
    const compsItems = compsArr.map((c) => {
      const name = String(c.name ?? '').replace(/_/g, ' ');
      const detail = c.que_evaluamos ? ` — ${c.que_evaluamos}` : '';
      return `<li><strong>${escapeHtml(name)}</strong>${escapeHtml(detail)}</li>`;
    });
    parts.push('<p><strong>Competencias clave a evaluar:</strong></p>');
    parts.push('<ul>' + compsItems.join('') + '</ul>');
  }
  return parts.join('');
}

function buildVentajasHtml(payload: Record<string, unknown>): string {
  const parts: string[] = [];
  const salario = typeof payload.salario === 'string' ? payload.salario : '';
  const range = payload.salary_range_usd as { min?: number; max?: number } | undefined;
  const modalidad = typeof payload.modalidad === 'string' ? payload.modalidad : '';
  const viajes = typeof payload.viajes === 'string' ? payload.viajes : '';

  if (salario) {
    parts.push(`<p><strong>Salario:</strong> ${escapeHtml(salario)}</p>`);
  } else if (range && (range.min || range.max)) {
    parts.push(`<p><strong>Salario:</strong> USD ${range.min ?? 0} – ${range.max ?? 0}</p>`);
  }
  if (modalidad) parts.push(`<p><strong>Modalidad:</strong> ${escapeHtml(modalidad)}</p>`);
  if (viajes) parts.push(`<p><strong>Viajes:</strong> ${escapeHtml(viajes)}</p>`);
  if (Array.isArray(payload.disc_ventajas) && payload.disc_ventajas.length > 0) {
    parts.push('<p><strong>Lo que va a poder lograr en este puesto:</strong></p>');
    parts.push(bulletList(payload.disc_ventajas));
  }
  return parts.join('');
}

